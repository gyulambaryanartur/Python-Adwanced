# align string to the left
print('{:*<10}'.format('message'))

# align string to the right
print('{:*>10}'.format('message'))

# align string to the center
print('{:*^10}'.format('message'))

# format number to 32bit binary
print('{:032b}'.format(16))

# floating point precision
print('{:.3f}'.format(1 / 3))
